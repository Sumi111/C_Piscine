int		ft_sqrt(int nb)
{
	int i;
	int result;

	i = 1;
	result = 1;
	while (result <= nb)
	{
		result = i * i;
		if (i > 46340)
			return (0);
		if (result == nb)
			return (i);
		i++;
	}
	return (0);
}
