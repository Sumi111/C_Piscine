int		ft_str_is_uppercase(char *str)
{
	int i;
	int temp;

	i = 0;
	if (str[0] == '\0')
	{
		return (1);
	}
	while (str[i] != '\0')
	{
		if ((str[i] >= 'A') && (str[i] <= 'Z'))
		{
			temp = 1;
		}
		else
			return (0);
		i++;
	}
	return (temp);
}
