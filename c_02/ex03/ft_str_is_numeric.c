int		ft_str_is_numeric(char *str)
{
	int i;
	int temp;

	if (str[0] == '\0')
	{
		return (1);
	}
	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= '0') && (str[i] <= '9'))
		{
			temp = 1;
		}
		else
			return (0);
		i++;
	}
	return (temp);
}
