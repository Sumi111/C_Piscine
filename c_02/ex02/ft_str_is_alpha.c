int		ft_str_is_alpha(char *str)
{
	int temp;
	int i;

	i = 0;
	if (str[0] == '\0')
	{
		temp = 1;
	}
	while (str[i] != '\0')
	{
		if ((str[i] >= 'a') && (str[i] <= 'z'))
		{
			temp = 1;
		}
		else if ((str[i] >= 'A') && (str[i] <= 'Z'))
		{
			temp = 1;
		}
		else
			return (0);
		i++;
	}
	return (temp);
}
