int	ft_atoi(char *str)
{
	int n;
	int sign;

	n = 0;
	sign = 1;
	while (*str == ' ')
		str++;
	while ((*str == '-') || (*str == '+'))
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	while (*str)
	{
		if ((*str >= '0') && (*str <= '9'))
			n = (n * 10) + ((*str - 48) % 10);
		else
			break ;
		str++;
	}
	return (n * sign);
}
